export function createT(lang) {
  const table = dict[lang] || dict.en;
  return function t(path, vars = {}) {
    const value = path.split('.').reduce((acc, key) => (acc == null ? acc : acc[key]), table);
    if (typeof value !== 'string') return path;
    return value.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`));
  };
}

export function getSection(lang, key) {
  return (dict[lang] || dict.en)[key];
}

